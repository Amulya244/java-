
public class Conversions 
{
	public static void main(String[] args) 
	{
		//convert int , double , bool, char to string format 
		int a =10;
		double d=10.5;
		char c='A';
		boolean bool=true;
		
		//for this conversion 
		//there is one method 
		//we can convert above date type to string 
		//that method is String.valueOf()\
		System.out.println(String.valueOf(a));
		System.out.println(String.valueOf(d));
		System.out.println(String.valueOf(c));
		System.out.println(String.valueOf(bool));
		//only this data conversions will convert 
		
	}
}
